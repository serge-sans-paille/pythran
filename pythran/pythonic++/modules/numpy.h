       template<class T, size_t N>
           decltype(std::declval<T>()+1.) median(core::ndarray<T,N> const& arr) {
               size_t n = arr.size();
               T* tmp = new T[n];
               std::copy(arr.buffer, arr.buffer + n, tmp);
               std::sort(tmp, tmp + n);
               auto out = (tmp[n/2]+tmp[(n-1)/2])/double(2);
               delete [] tmp;
               return out;
           }

